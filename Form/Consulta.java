    private void consultarViaList(Veiculo v) {
        renderSelectedVeiculo(Database.selectVeiculo(v.getPlaca(), null));
    }

    private void consultar() {

        if (!tfCodPneu.getText().isEmpty() || !tfPlaca.getText().isEmpty()) {
            Veiculo v = Database.selectVeiculo(tfPlaca.getText(), tfCodPneu.getText());
            if (v.getPlaca() != null) {
                renderSelectedVeiculo(v);
                
            } else {
                String param = tfCodPneu.getText().isEmpty() ? tfPlaca.getText() : tfCodPneu.getText();
                String paramName = tfCodPneu.getText().isEmpty() ? "[Placa: " : "[C√≥digo Pneu: ";
                JOptionPane.showMessageDialog(this, "Ve√≠culo n√£o encontrado com o par√¢metro informado " + paramName + param + "]");
            }

        } else {
            JOptionPane.showMessageDialog(this, "Favor preencher um dos campos para executar a pesquisa !");
        }
    }

    private void renderSelectedVeiculo(Veiculo v) {

        formulario1.veicIdFromConsulta = v.getId();
        formulario1.getTfPlaca().setText(v.getPlaca());
        formulario1.getTfModelo().setText(v.getModelo());
        formulario1.getTfKm().setText(String.valueOf(v.getKm()));

        formulario1.getTfPeso().setText(String.valueOf(v.getPeso()));
        formulario1.getTfMarca().setText(v.getMarca());
        formulario1.getImagePanel1().carregaImagem(v.getImg());
        loadComboTipoVeiculo(DatabaseTipo.selecteTipo(v.getIdTipo()).getNome());
        formulario1.getTfAno().setText(v.getAno());

        for (Pneu p : v.getPneus()) {
            formulario1.insertPneuInTable(p);
        }

        panelConsulta.setVisible(false);
        formulario1.setVisible(true);
        formulario1.setHabilitarEdicaoVisible(true);
        formulario1.setComponentsEditable(false);
        formulario1.isEdicao = true;
        formulario1.loadIconComponent();

        JFrame topFrame = (JFrame) SwingUtilities.getWindowAncestor(this);
        topFrame.pack();
        topFrame.setLocationRelativeTo(null);
    }

    private void loadComboTipoVeiculo(String nome) {

        TipoVeiculo item;
        for (int i = 0; i < formulario1.getComboTipo().getItemCount(); i++) {
            item = (TipoVeiculo) formulario1.getComboTipo().getItemAt(i);
            if (item.getNome().equals(nome)) {
                formulario1.getComboTipo().setSelectedIndex(i);
                break;
            }
        }
    }
