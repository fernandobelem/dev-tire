    private void comboEixoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_comboEixoItemStateChanged
        if (evt.getStateChange() == ItemEvent.SELECTED) {
            if (comboEixo.getSelectedIndex() != 0) {
                comboPosicao.setEnabled(true);
            } else {
                comboPosicao.setEnabled(false);
            }
            
            if(((String)comboEixo.getSelectedItem()).equals("Estepe")){
                comboPosicao.setSelectedIndex(comboPosicao.getItemCount() -1);
                comboPosicao.setEnabled(false);
            }
                

        }
    }//GEN-LAST:event_comboEixoItemStateChanged

