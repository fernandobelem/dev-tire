    private void configureComboPosicaoPneu(TipoVeiculo t) {
        comboPosicao.setEnabled(false);
        comboPosicao.addItem("Selecione");

        if (t.getNome().equals("Veículo Passeio")) {
            comboPosicao.addItem("Pneu esquerdo");
            comboPosicao.addItem("Pneu direito");
            comboPosicao.addItem("Estepe");
            return;
        }
        if (t.getRodasPorEixo() == 2) {
            comboPosicao.addItem("Pneu esquerdo");
            comboPosicao.addItem("Pneu direito");
        }

        if (t.getRodasPorEixo() > 2) {
            comboPosicao.addItem("Pneu externo esquerdo");
            comboPosicao.addItem("Pneu externo direito");
            comboPosicao.addItem("Pneu interno direito");
            comboPosicao.addItem("Pneu interno esquerdo");

        }
        comboPosicao.addItem("Estepe");

    }

    private void configureComboEixo(TipoVeiculo t) {
        comboEixo.addItem("Selecione");

        if (t.getNome().equals("Veículo Passeio")) {
            labelEixo.setText("Orientação Pneu: ");
            comboEixo.addItem("Dianteiro");
            comboEixo.addItem("Traseiro");
            comboEixo.addItem("Estepe");

            return;
        }

        if (t.getNumeroEixos() == 2) {
            comboEixo.addItem("Eixo frontal");
            comboEixo.addItem("Eixo traseiro");

        } else {
            for (int i = 1; i <= t.getNumeroEixos(); i++) {
                comboEixo.addItem("Eixo " + String.valueOf(i));
            }
        }

        comboEixo.addItem("Estepe");

    }

