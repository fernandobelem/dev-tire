    private void incluiPneu() {

        if (!tfCodPneu.getText().isEmpty()) {
            if (contemDuplicados(tfCodPneu.getText())) {
                JOptionPane.showMessageDialog(this, "Este código já existe, favor alterar!");
                return;
            }

            if (validatePosicao((String) comboPosicao.getSelectedItem(), (String) comboEixo.getSelectedItem())) {
                JOptionPane.showMessageDialog(this, "Está posição já foi selecionada, favor selecionar uma nova posição !");
                return;
            }

            if (selectedPosEqualsZero()) {
                JOptionPane.showMessageDialog(this, "Favor selecionar uma posição para o Pneu");
                return;
            }

            if (comboEixo.getSelectedIndex() == 0) {
                JOptionPane.showMessageDialog(this, "Favor selecionar um eixo");
            }

            Pneu pneu = new Pneu();
            pneu.setCodigo(tfCodPneu.getText());
            pneu.setPosicao((String) comboPosicao.getSelectedItem());
            pneu.setEstado((String) comboEstado.getSelectedItem());
            pneu.setEixo((String) comboEixo.getSelectedItem());
        	f.insertPneuInTable(pneu);
            f.getComboTipo().setEnabled(false);
            JOptionPane.showMessageDialog(this, "Pneu incluso com sucesso !");
            this.dispose();

        } else {
            JOptionPane.showMessageDialog(this, "Insira o código do pneu antes de confirmar !");
        }

    }

    private boolean selectedPosEqualsZero() {
        return comboPosicao.getSelectedIndex() == 0;
    }

    private boolean validatePosicao(String pos, String eixo) {
        for (Pneu p : f.getListPneu()) {
            if (p.getPosicao().equals(pos) && p.getEixo().equals(eixo)) {
                return true;
            }
        }
        return false;
    }

    private boolean contemDuplicados(String cod) {
        return existeCodigoNaLista(cod) || existeCodigoNaBase(cod);
    }

    private boolean existeCodigoNaBase(String cod) {
        List<Pneu> list = Database.selectAllPneuByCod(cod);
        for (Pneu p : list) {
            if (p.getCodigo().equals(cod)) {
                return true;
            }
        }
        return false;
    }

    private boolean existeCodigoNaLista(String cod) {
        for (Pneu p : f.getListPneu()) {
            if (p.getCodigo().equals(cod)) {
                return true;
            }
        }
        return false;
    }


